# Función
def grafica_frecuencias(lista):
    # Iterar cada elemento de la lista
    for p in lista:
        # Imprimir el nombre del producto agregando un espacio
        print(p["producto"]," ",end="")
        # Iterar desde 0 hasta la frecuencua en el diccionario p
        for f in range(p["f"]):
            # Imprimir 0 en cada iteración agregando un espacio
            print("0",end="")
            # Imprimir un nuevo espacio
        print("")



   


        
    
